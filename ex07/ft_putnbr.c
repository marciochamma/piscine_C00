#include<unistd.h>

void	ft_putnbr(int nb)
{
	int	u;

	if (nb < 0)
	{
		write(1, "-", 1);
		u = (nb % 10) * -1 + 48;
		nb = nb / -10;
	}
	else
	{
		u = (nb % 10) + 48;
		nb = nb / 10;
	}
	if (nb != 0)
	{
		ft_putnbr(nb);
	}
	write(1, &u, 1);
}
